m = {'кот',  'псина', 'информатика', 'превысокомногорассмотрительствующий', 'зачёты', 'частнопредпринимательский', 'дом', 'буква', 'множество', 'гипербола', 'кабан', 'проект', 'файл', 'число', 'фильм', 'земля', 'лодка', 'вагон', 'вертолёт', 'наука', 'шашлык', 'зима', 'человек', 'день', 'тест', 'телефон', 'музей', 'гравитация', 'самолёт', 'стекло', 'правило', 'схема', 'таблица', 'фото', 'периодизация', 'бургер'} # множество слов
for i in range(len(m)): # игра будет продолжаться пока не закончаться слова
	word = m.pop()
	s = ['-'] * len(word) # зашифрованное слово
	rules = 'Вводите буквы (по одной или сразу всё слово) до тех пор, пока полностью не отгадаете слово, в конце вы увидите ваши попытки, чем больше попыток тем хуже вы справились.' # правила
	instruction = 'Список команд: /help (выводит правила), /сдаться, /letters_used(выводит список использовнных букв), /remaining_letters(выводит список из оставшихся букв).' # список команд
	print(rules)
	print(instruction)
	a = input().lower() # ввод(не важно какой регистр)
	count = 0 # количество попыток
	letters_used = [] # список использованных букв
	remaining_letters = list(word) # список не использованных букв
	print(''.join(s.copy()))
	while a != '/сдаться':
		if a == word: # проверка на совпадение введённого слова и загаданного
			print('Вы отгадали слово!')
			print('Ваши попытки:', count)
			break
		if a == '/help': # проверка на ввод команды
			print(rules, instruction, end='\n')
			a = input()
			continue
		if a == '/letters_used': # проверка на ввод команды
			print(letters_used)
			a = input()
			continue
		if a == '/remaining_letters': # проверка на ввод команды
			print(remaining_letters)
			a = input()
			continue
		if s == list(word): # проверка на совпадение букв с загаднным словом
			print('Вы отгадали слово!')
			print('Ваши попытки:', count)
			break
		if a.isdigit(): # проверка на ввод не слова/буквы
			print('Введите букву или всё слово!')
			a = input()
			continue
		if a in word and a not in letters_used and a != '': # проверка на нахождение буквы в слове и её отсутствие в списке использованных букв
			for j in range(len(s)):
				if word[j] == a:
					s[j] = a # заменяем все нижнии подчёркивания на одинаковые отгаданные буквы в зашифрованном слове
					letters_used.append(a) # добавлем в список использованных букв букву
					remaining_letters1 = [] # в этом списке будут хранится все не использованные буквы
			for k in range(len(remaining_letters)):
				if remaining_letters[k] != a:
					remaining_letters1.append(remaining_letters[k]) # добавляем все буквы загаданного слова, кроме отгаданной
			remaining_letters = []
			for el in remaining_letters1:
				remaining_letters.append(el) # обновляем список не использованых букв
			s1 = s.copy()
			print('Вы угадали букву!', ''.join(s1)) # вывод зашифрованного слова с отгаданными буквами
		elif a in word and a in letters_used: # проверка на нахождение буквы в слове и её присутствие в списке использованных букв
			print('Вы уже вводили эту букву!')
		elif a not in word: # проверка на отсутствте буквы в слове
			print('Попробуйте снова! Вы точно отгадаете!(не факт)')
		a = input().lower()
		count += 1 # увеличиваем количество попыток
	if a == '/сдаться':
		print('Вы проиграли!')